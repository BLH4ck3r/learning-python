
def sumar_numeros(num1, num2):
    return num1 + num2

def restar_numeros(num1, num2):
    return num1 - num2

def multiplicar_numeros(num1, num2):
    return num1 * num2

def dividir_numeros(num1, num2):
    return num1 / num2

def potencia_de_un_numero(num1, num2):
    return num1 ** num2

def raiaz_de_un_numero(num1, num2):
    return num1 ** (1/num2)
